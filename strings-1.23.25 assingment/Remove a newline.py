# String with a newline
string_with_newline = "Hello, World!\nWelcome to Anudip Python programming class."

# Remove the newline using .replace()
string_without_newline = string_with_newline.replace("\n", "")

print("Original string:")
print(string_with_newline)
print("\nString without newline:")
print(string_without_newline)
